#Numerators
numerator1 = 3
numerator2 = 7
#Denominators
denominator1 = 6
denominator2 = 28
#fractions
fraction1 = numerator1 / denominator1
fraction2 = numerator2 / denominator2
#PrintFractions
print("Fraction 1 is", numerator1,"/", denominator1)
print("Fraction 2 is", numerator2,"/", denominator2)
#MultFractions
FracProduct = fraction1 * fraction2
#DivFractions
FracDiv = fraction1 / fraction2
#PrintDivFrac
print("Fraction 1 divided by fraction 2 is", FracDiv)
print("Fraction 1 multiplied by fraction 2 is", FracProduct)